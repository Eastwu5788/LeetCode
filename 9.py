"""
判断一个整数是否是回文数。回文数是指正序（从左向右）和倒序（从右向左）读都是一样的整数。

示例 1:

输入: 121
输出: true
"""


class Solution(object):

    def isPalindrome(self, x):
        """
        :type x: int
        :rtype: bool
        """
        if x < 0:
            return False

        reverse, tmp = 0, x
        while tmp > 0:
            reverse = reverse * 10 + tmp % 10
            tmp = tmp // 10

        return reverse == x


if __name__ == "__main__":
    print(Solution().isPalindrome(121))
